package org.beetl.core.misc;

import org.junit.Assert;
import org.junit.Test;

public class BeetlUtilTest {
	
	@Test
	public void checkNameing(){
		for (int i = 0; i < 1000000; i++) {
			Assert.assertTrue("checkNameing错误,验证:小写字母",BeetlUtil.checkNameing("abcdefghijklmnopqrstuvwxyz"));
			Assert.assertTrue("checkNameing错误,验证:大写字母",BeetlUtil.checkNameing("ABCDEFGHIJKLMNOPQRSTUVWXYZ"));
			Assert.assertTrue("checkNameing错误,验证:$符号", BeetlUtil.checkNameing("$$"));
			Assert.assertTrue("checkNameing错误,验证:_符号", BeetlUtil.checkNameing("__"));
			Assert.assertTrue("checkNameing错误,验证:.符号", BeetlUtil.checkNameing("a.a"));
			Assert.assertFalse("checkNameing错误,验证:.符号", BeetlUtil.checkNameing(".a"));
			Assert.assertFalse("checkNameing错误,验证:.符号", BeetlUtil.checkNameing("a."));
			Assert.assertFalse("checkNameing错误,验证:其他符号", BeetlUtil.checkNameing(" !@#$%^&*()"));
		}
	}
}
